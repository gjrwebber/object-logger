package org.gw.objectlogger;

public class SerialisationException extends Exception {

	/**
     * 
     */
	private static final long serialVersionUID = 1L;

	public SerialisationException() {
		super();
	}

	public SerialisationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SerialisationException(String arg0) {
		super(arg0);
	}

	public SerialisationException(Throwable arg0) {
		super(arg0);
	}

}
